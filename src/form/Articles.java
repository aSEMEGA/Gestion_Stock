    //Liste
    private void lister() {
        ProduitDao dao = new ProduitDao();
        List<Produit> e = new ArrayList();
        e = dao.liste();
        for (int i = 0; i < e.size(); i++) {
            ((DefaultTableModel) jTable1.getModel()).addRow(new Object[]{
                e.get(i).getId(),
                e.get(i).getCode_produit(),
                e.get(i).getDesignation(),
                e.get(i).getPrix(),
                e.get(i).getStock()
               
                });

        }
    }
    
    
    private void vider() {
        cod.setText("");
        des.setText("");
        ps.setText("");
        st.setText("");
        

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int nbr_ligne = model.getRowCount();
        for (int i = nbr_ligne - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }
