    private void AjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AjouterActionPerformed
        ProduitDao dao = new ProduitDao();
        String code = cod.getText();
        String designation = des.getText();
        String prix = ps.getText();
        String stock = st.getText();
        
        try{
           if (designation.equals("") || prix.equals("")|| stock.equals("") || code.equals("")){
               JOptionPane.showMessageDialog(this, "RENSEIGNER LES CHAMPS SVP");
           }else{
               Produit p = new Produit(code,designation,prix,stock);
               dao.ajouter(p);
               vider();
               lister();

           }
       }catch (Exception e){
            e.printStackTrace();
       }
    }//GEN-LAST:event_AjouterActionPerformed

    private void ModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifierActionPerformed
        ProduitDao dao = new ProduitDao();
        String code = cod.getText();
        String designation = des.getText();
        String prix = ps.getText();
        String stock = st.getText();
        int id = jTable1.getSelectedRow();
        int select = (int) jTable1.getValueAt(id, 0);
        Produit p = new Produit(code,designation,prix,stock);
        dao.modifier(p,select);
        vider();
        lister();
        
    }//GEN-LAST:event_ModifierActionPerformed

