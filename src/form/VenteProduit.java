    private void selectprod() {
        ProduitDao dao = new ProduitDao();
        List<Produit> e = new ArrayList();
        e = dao.liste();
        for (int i = 0; i < e.size(); i++) {
            ((DefaultTableModel) jTable1.getModel()).addRow(new Object[]{
                e.get(i).getId(),
                e.get(i).getCode_produit(),
                e.get(i).getDesignation(),
                e.get(i).getPrix(),
                e.get(i).getStock()
               
                });

        }
    }
