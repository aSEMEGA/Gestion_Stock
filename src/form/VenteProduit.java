    private void lister() {
        VenteDao dao = new VenteDao();
        List<Vente> v = new ArrayList();
        v = dao.liste();
        for (int i = 0; i < v.size(); i++) {
            ((DefaultTableModel) jTable2.getModel()).addRow(new Object[]{
                v.get(i).getId(),
                v.get(i).getCode_produit(),
                v.get(i).getDesignation(),
                v.get(i).getPrix_vente(),
                v.get(i).getStock_sortie(),
               v.get(i).getMaitre()
                });

        }
    }
    
    
    private void vider() {
        cds.setText("");
        combo.setSelectedItem("");
        pv.setText("");
        sts.setText("");
        maitre.setText("");
        

        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        int nbr_ligne = model.getRowCount();
        for (int i = nbr_ligne - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }
